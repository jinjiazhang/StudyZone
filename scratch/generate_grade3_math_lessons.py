OUT_DIR = "apps/api/prisma/lesson-data/math/grade-3-volume-1"

def main():
    print("Generating Math Grade 3 Volume 1 Lesson Data...")
    
    for unit_dir, unit_data in DATASET.items():
        unit_path = os.path.join(OUT_DIR, unit_dir)
        os.makedirs(unit_path, exist_ok=True)
        
        # 1. Create lessons.json for this unit
        lessons_meta = {
            "lessons": []
        }
        for idx, lesson in enumerate(unit_data["lessons"]):
            lessons_meta["lessons"].append({
                "file": lesson["file"],
                "orderIndex": idx,
                "title": lesson["title"],
                "icon": lesson["icon"]
            })
            
        with open(os.path.join(unit_path, "lessons.json"), "w", encoding="utf-8") as f:
            json.dump(lessons_meta, f, indent=2, ensure_ascii=False)
            
        # 2. Generate each lesson levels file containing exactly 25 exercises
        for lesson in unit_data["lessons"]:
            exercises = []
            l_type = lesson["type"]
            
            # The calculation types which have 2-element questions (expr, value)
            calc_types = ["mixed_add_sub", "mixed_div", "parentheses", "three_digit_add", "three_digit_sub", "three_digit_mixed", "mental_tens_mul", "mental_double_mul", "mental_tens_div", "mental_double_div", "mul_no_carry", "mul_carry_once", "mul_carry_multi", "mul_with_zero", "mul_three_numbers", "decimals_add_sub_easy", "decimals_add_sub_hard"]
            
            if l_type in calc_types:
                # Generates math calculations (numeric_input) and matches (match_pairs) and word problems (single_choice)
                questions = lesson["questions"]
                word_problems = lesson["word_problems"]
                
                # ----------------------------------------------------
                # Type 1: numeric_input (16 questions)
                # ----------------------------------------------------
                for expr, val in questions[:16]:
                    exercises.append({
                        "type": "numeric_input",
                        "prompt": {
                            "statement": f"{expr} = ?"
                        },
                        "answer": {
                            "value": float(val),
                            "tolerance": 0.01
                        },
                        "difficulty": 1
                    })
                    
                # ----------------------------------------------------
                # Type 2: match_pairs (6 questions)
                # ----------------------------------------------------
                for i in range(6):
                    # Sample 4 calculations
                    sample_q = random.sample(questions, 4)
                    left = [{"id": f"l_{idx}", "text": f"{q[0]} = ?"} for idx, q in enumerate(sample_q)]
                    right = [{"id": f"r_{idx}", "text": f"{q[1]}"} for idx, q in enumerate(sample_q)]
                    random.shuffle(right)
                    
                    pairs_map = {}
                    for l_item in left:
                        expr_clean = l_item["text"].replace(" = ?", "")
                        matching_val = next(q[1] for q in sample_q if q[0] == expr_clean)
                        r_item = next(r for r in right if r["text"] == str(matching_val))
                        pairs_map[l_item["id"]] = r_item["id"]
                        
                    exercises.append({
                        "type": "match_pairs",
                        "prompt": {
                            "left": left,
                            "right": right
                        },
                        "answer": {
                            "pairs": pairs_map
                        },
                        "difficulty": 2
                    })
                    
                # ----------------------------------------------------
                # Type 3: single_choice (3 questions)
                # ----------------------------------------------------
                for i in range(3):
                    prob_desc, formula, val = word_problems[i % len(word_problems)]
                    correct_opt = f"{val} ({formula.split()[-1]})" if len(formula.split()) > 1 else f"{val}"
                    distractors = [f"{val + 5}", f"{val - 2 if val > 2 else val + 10}", f"{val * 2}"]
                    options = [correct_opt] + distractors
                    random.shuffle(options)
                    correct_idx = options.index(correct_opt)
                    
                    exercises.append({
                        "type": "single_choice",
                        "prompt": {
                            "question": f"{prob_desc} 请列式并选择正确答案：",
                            "options": options
                        },
                        "answer": {
                            "correctIndex": correct_idx
                        },
                        "difficulty": 2
                    })
            else:
                # Conceptual units (observe_basics, observe_advanced, odometer, perimeter_basics, perimeter_concept, perimeter_combos, travel_multi_step, calendar_basics, time_notation, time_duration, decimals_basics, decimals_compare, decimals_word_problems)
                questions = lesson["questions"]
                concept_matches = lesson["concept_matches"]
                
                # ----------------------------------------------------
                # Type 1: single_choice (16 questions)
                # ----------------------------------------------------
                for i in range(16):
                    q_text, opts, correct_idx = questions[i % len(questions)]
                    exercises.append({
                        "type": "single_choice",
                        "prompt": {
                            "question": q_text,
                            "options": opts
                        },
                        "answer": {
                            "correctIndex": correct_idx
                        },
                        "difficulty": 2
                    })
                    
                # ----------------------------------------------------
                # Type 2: match_pairs (9 questions)
                # ----------------------------------------------------
                for i in range(9):
                    sample_matches = list(concept_matches)
                    while len(sample_matches) < 4:
                        sample_matches.append(("数学好玩", "提升数学兴趣"))
                    sample_matches = random.sample(sample_matches, 4)
                    
                    left = [{"id": f"l_{idx}", "text": m[0]} for idx, m in enumerate(sample_matches)]
                    right = [{"id": f"r_{idx}", "text": m[1]} for idx, m in enumerate(sample_matches)]
                    random.shuffle(right)
                    
                    pairs_map = {}
                    for l_item in left:
                        matching_val = next(m[1] for m in sample_matches if m[0] == l_item["text"])
                        r_item = next(r for r in right if r["text"] == matching_val)
                        pairs_map[l_item["id"]] = r_item["id"]
                        
                    exercises.append({
                        "type": "match_pairs",
                        "prompt": {
                            "left": left,
                            "right": right
                        },
                        "answer": {
                            "pairs": pairs_map
                        },
                        "difficulty": 2
                    })
                    
            # Double check we have exactly 25 exercises
            with open(os.path.join(unit_path, lesson["file"]), "w", encoding="utf-8") as f:
                json.dump({"exercises": exercises[:25]}, f, indent=2, ensure_ascii=False)
                
    print("Math Grade 3 Volume 1 Lesson Data Generated Successfully!")

if __name__ == "__main__":
    main()
