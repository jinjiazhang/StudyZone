            all_pinyins = [v[1] for v in vocab]
            
            # ----------------------------------------------------
            # Type 1: pinyin_choice (6 questions)
            # ----------------------------------------------------
            for i in range(6):
                char, py, _ = vocab[i % len(vocab)]
                distractors = list(set(all_pinyins) - {py})
                # pad distractors if needed
                while len(distractors) < 3:
                    distractors.append("pīn")
                options = [py] + random.sample(distractors, 3)
                random.shuffle(options)
                correct_idx = options.index(py)
                
