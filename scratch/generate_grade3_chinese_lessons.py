                right = [{"id": f"r_{idx}", "text": p[1]} for idx, p in enumerate(sample_pairs)]
                random.shuffle(right)
                
                pairs_map = {}
                for l_item in left:
                    matching_zh = next(p[1] for p in sample_pairs if p[0] == l_item["text"])
                    r_item = next(r for r in right if r["text"] == matching_zh)
                    pairs_map[l_item["id"]] = r_item["id"]
                    
                exercises.append({
                    "type": "match_pairs",
                    "prompt": {
                        "left": left,
                        "right": right
                    },
                    "answer": {
                        "pairs": pairs_map
                    },
                    "difficulty": 2
                })
                
